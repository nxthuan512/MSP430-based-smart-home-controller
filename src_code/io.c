#include "msp430f5438.h"

// ================================================================
//
// ================================================================
void port1_clear()
{
	// Port 1: output, default values 0xff
	P1DIR = 0xff;
	P1OUT = 0xff;		
}

// ================================================================
//
// ================================================================
void port1_initialize()
{
	port1_clear();
	
	// End Port1 Init -> Led 0
	P1OUT = P1OUT & 0xfe;
}
